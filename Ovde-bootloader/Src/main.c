int main(void)
{

  /* USER CODE BEGIN 1 */

	MX_GPIO_Init();
	MX_TIM2_Init();
	// We kijken of de knop ingedrukt is
	if( !HAL_GPIO_ReadPin( GPIOI, GPIO_PIN_11 ) && *pucUserAppName != 15 )
	{
		// Als de knop niet ingedrukt is springen we naar het programma in flash
		vObootloaderJumpToApplication( otcpAPPLICATION_ADDRESS );
	}

	if( *pucUserAppName == 15 )
	{
		// We zijn herstart zoals gevraagd over udp, nu terug naar normale werking
		// We moeten dus terug 14 voor de user-app naam schrijven
		HAL_FLASH_Unlock();
		HAL_FLASH_Program( FLASH_TYPEPROGRAM_BYTE, otcpAPPLICATION_NAME_ADDRESS, 14 );
		HAL_FLASH_Lock();
	}

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_LTDC_Init();
  MX_DMA2D_Init();
  MX_USART1_UART_Init();
  MX_LWIP_Init();
  MX_TIM2_Init();
  MX_FMC_Init();

  /* USER CODE BEGIN 2 */

  struct ip4_addr xServerIp;
  struct tcp_pcb * pxTcpConnectie;

  IP4_ADDR( &xServerIp, 192, 168, 2, 2 );

  // We maken een nieuwe tcp server en laten deze luisteren op poort 5555
  pxTcpConnectie = tcp_new();
  tcp_bind( pxTcpConnectie, &xServerIp, otcpBOOTLOADER_SERVER_PORT );
  pxTcpConnectie = tcp_listen_with_backlog( pxTcpConnectie, 1 );
  if( pxTcpConnectie != NULL )
  {
	  tcp_accept( pxTcpConnectie, xMainClientConnected );
  }



  // We initialiseren udp zodat we op het netwerk gevonden kunnen worden via een broadcast
  vOdiscoverInitUdpDiscover();


  // We tonen een welcomeMessage op het lcd
  BSP_SDRAM_Init();
  vOtextInitLcd( &hltdc, &hdma2d, mainLCD_FB_START_ADDRESS );
  vOtextWriteString( 184, 50, otcpWELCOME );

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while ( 1 )
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

	  MX_LWIP_Process();

  }
  /* USER CODE END 3 */

}

